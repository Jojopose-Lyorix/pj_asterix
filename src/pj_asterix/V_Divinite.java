    private void bt_suppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_suppActionPerformed
        if (table_divinite.getSelectedRow() == -1) {
        JOptionPane.showMessageDialog(this, "une divinité doit être sélectionné dans le tableau", "Sélection obligatoire", JOptionPane.ERROR_MESSAGE);
    } else {
        iDivi = table_divinite.getSelectedRow();
        int id = (Integer) table_divinite.getValueAt(iDivi, 0);  // ← L'ID est ici !
        
        int reponse = JOptionPane.showConfirmDialog(this,
            "Êtes-vous sûr de vouloir supprimer la divinité ?",
            "Attention !",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );

        if (reponse == JOptionPane.YES_OPTION) {
            try {
                controler.supp_divi(id);  // ✅ Passer id, pas iDivi !
            } catch (java.sql.SQLException ex) {
                System.getLogger(V_Divinite.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    }
    }//GEN-LAST:event_bt_suppActionPerformed

