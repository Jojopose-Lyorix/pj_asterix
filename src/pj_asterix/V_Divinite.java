    private void sePlacer() {
        iDivi = table_divinite.getSelectedRow();

        int id = (Integer) table_divinite.getValueAt(iDivi, 0);

        unDieu = lesDivinites.get(id);
        tx_id.setText(String.valueOf(unDieu.getId()));
        tx_nom.setText(unDieu.getNom());
        tx_com.setText(unDieu.getCommentaire());
        tx_con.setText(unDieu.getContexte());
  }

    private void ihm(char mode) {
        if (mode == 'T') {
            jpan_divi.setVisible(true);
            bt_detail.setVisible(true);
        } else {
            switch (mode) {
                case 'C' -> {
                    lb_titre.setText("Consultationner");
                    this.activation(false);
                    jpan_divi.setVisible(false);
                    this.setSize(560, 400);
                    sePlacer();
                    bt_detail.setVisible(true);
                    bt_retour.setVisible(true);

                    bt_modif.setVisible(false);
                    bt_annuler.setVisible(false);
                }
                case 'A' -> {
                    lb_titre.setText("Ajoutationner");
                    this.activation(true);
                    jpan_divi.setVisible(false);
                    this.setSize(560, 400);
                    bt_detail.setVisible(true);
                    bt_enre.setVisible(true);
                    bt_annuler.setVisible(true);
                    bt_retour.setVisible(false);
                    clear();
                }
                case 'M' -> {
                    lb_titre.setText("Modificationner");
                    this.activation(true);
                    jpan_divi.setVisible(false);
                    this.setSize(560, 400);
                    sePlacer();
                    bt_detail.setVisible(true);
                    bt_enre.setVisible(true);
                    bt_annuler.setVisible(true);
                    bt_retour.setVisible(false);
                }

            }
        }
    }
