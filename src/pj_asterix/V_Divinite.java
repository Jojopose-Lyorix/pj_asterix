    private void bt_enreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_enreActionPerformed
        String vNom = tx_nom.getText().trim();
    String vCom = tx_com.getText().trim();
    String vCon = tx_con.getText().trim();

    if (vNom.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Le nom est obligatoire.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return;
    }

    boolean isModif = "Modificationner".equals(lb_titre.getText());

    if (isModif) {
        int vidPerso = Integer.parseInt(tx_id.getText());

        int reponse = JOptionPane.showConfirmDialog(
            this,
            "Êtes-vous sûr de vouloir modifier " + vNom + " ?",
            "Confirmation",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );

        if (reponse == JOptionPane.YES_OPTION) {
            try {
                controler.enregistrer_divi(vidPerso, vNom, vCom, vCon);
                afficher(controler, lesDivinites);
            } catch (java.sql.SQLException ex) {
                JOptionPane.showMessageDialog(this,
                    "Erreur lors de la modification : " + ex.getMessage(),
                    "Erreur SQL/IO",
                    JOptionPane.ERROR_MESSAGE);
                logger.log(java.util.logging.Level.SEVERE, ex.getMessage(), ex);
            } catch (IOException ex) {
                System.getLogger(V_Divinite.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    } else {
        int reponse = JOptionPane.showConfirmDialog(
            this,
            "Êtes-vous sûr de vouloir ajouter " + vNom + " ?",
            "Confirmation",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );

        if (reponse == JOptionPane.YES_OPTION) {
            try {
                controler.ajouter_divi(vNom, vCom, vCon);
                afficher(controler, lesDivinites);
            } catch (IOException ex) {
                System.getLogger(V_Divinite.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            } catch (java.sql.SQLException ex) {
                System.getLogger(V_Divinite.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    } 
    }//GEN-LAST:event_bt_enreActionPerformed

    /**
     *
     * @param gestionPerso
     * @param lesDivinites
     */
    
    public void afficher(C_asterix gestionPerso, LinkedHashMap<Integer, M_divinites> lesDivinites) {
        this.lesDivinites = lesDivinites;
        this.controler = gestionPerso;

        dm_tb_Divinites = (DefaultTableModel) table_divinite.getModel();
        dm_tb_Divinites.setRowCount(lesDivinites.size());
        ligne = 0;

        
        for (Integer uneCle : lesDivinites.keySet()) {
            unDieu = lesDivinites.get(uneCle);
            table_divinite.setValueAt(unDieu.getId(), ligne, 0);
            table_divinite.setValueAt(unDieu.getNom(), ligne, 1);
           
            ligne++;
            
        }
        
       

        this.setSize(600, 400);
        this.setLocationRelativeTo(null);

        bt_detail.setVisible(true);
        jpan_divi.setVisible(true);
        this.ihm('T');
        
        
        setVisible(true);
    }
